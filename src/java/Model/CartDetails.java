package Model;

/**
 *
 * @author Thinkpad
 */
public class CartDetails {
    private int id;
    private String quantity;

    public CartDetails() {
    }

    public CartDetails(int id, String quantity) {
        this.id = id;
        this.quantity = quantity;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }
    
}
